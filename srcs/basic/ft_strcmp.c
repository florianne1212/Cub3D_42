#include "cub3d.h"

int		ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	e;

	i = 0;
	e = 0;
	if (s1 == NULL || s2 == NULL)
		return (-1);
	while (*(s1 + i) != '\0')
	{
		if (*(s1 + i) < *(s2 + i))
		{
			e = *(s1 + i) - *(s2 + i);
			return (e);
		}
		else if (*(s1 + i) > *(s2 + i))
		{
			e = *(s1 + i) - *(s2 + i);
			return (e);
		}
		else if (*(s1 + i) == *(s2 + i))
		{
			i++;
		}
	}
	return (e);
}
